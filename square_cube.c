#include<stdio.h>

int main()
{
	int a, b, square, cube;
	
		printf("Enter the number a: ",a);
	    scanf("%d", &a);
    	square=a*a;
    	printf("%d*%d=%d\n", a,a,square);
	
		printf("Enter the number b: ",b);
	    scanf("%d", &b);
    	cube=b*b*b;
    	printf("%d*%d*%d=%d", b,b,b,cube);
	
	
}
